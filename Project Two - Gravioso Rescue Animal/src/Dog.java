public class Dog extends RescueAnimal {

    // Instance variable
    public String breed;

    // Constructor
    public Dog() {
    }

    // Accessor (getters) Method
    public String getBreed() {
        return breed;
    }

    // Mutator (setters) Method
    public void setBreed(String dogBreed) {
        breed = dogBreed;
    }

}